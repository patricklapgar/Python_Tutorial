# Every string has an index within it
# e.g. "lol" has indicies ranging from 0 - n (in this case, 0 - 2)
# To access the specific index of a string, use [] like so
name = "Patrick"
print(name[0]) # Returns 'P'

# Here's a cool thing!! You can also access indicies of a string with negative numbers
print(name[-1]) # Prints the last index of the string ('k')
print(name[-2]) # Prints the second-to-last index of the string ('c')
# And so on and so forth.

